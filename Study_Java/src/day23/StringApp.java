package day23;

public class StringApp {
    public static void main(String[] args) {

        // new line (줄바꿈)
        System.out.println("Hello \nWorld");
        // escape
        System.out.println("Hello \"World\""); // Hello "World"
        // replace
        System.out.println("안녕하세요. 잘 지내시죠? [[[Name]]]씨?".replace("[[[Name]]]","홍길동"));


    }
}
